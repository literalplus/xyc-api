package li.l1t.common.sql.sane.result;

import java.sql.ResultSet;

/**
 * Represents the result of an update statement issued to a SQL data source.
 *
 * @author <a href="https://l1t.li/">Literallie</a>
 * @since 2016-10-09
 */
public interface UpdateResult extends CloseableResult {
    /**
     * @return the result set containing the values of the keys that were generated by the update
     * statement, if any
     */
    ResultSet gk();

    /**
     * @return the amount of rows affected by the update, according to the driver
     */
    int getAffectedRowCount();
}
